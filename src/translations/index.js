export function t(key, lang = 'pt') {
  if (!translations[lang]) return key
  
  // Suporte para propriedades aninhadas (ex: contentTypes.article)
  const keys = key.split('.')
  let value = translations[lang]
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k]
    } else {
      return key // Retorna a chave original se não encontrou
    }
  }
  
  return value || key
}
